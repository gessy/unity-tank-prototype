namespace TankProto
{
	/// <summary>
	/// Container for variables that are accessed througout
	/// different places.
	/// </summary>
	public struct GlobalVariables
	{
		/// <summary>
		/// Horizontal axis input name.
		/// </summary>
		public const string HorizontalAxis = "Horizontal";

		/// <summary>
		/// Vertical axis input name.
		/// </summary>
		public const string VerticalAxis = "Vertical";

		/// <summary>
		/// Index of environment layer.
		/// </summary>
		public const int EnvironmentLayer = 8;

		/// <summary>
		/// Index of character layer.
		/// </summary>
		public const int CharacterLayer = 9;
	}
}
